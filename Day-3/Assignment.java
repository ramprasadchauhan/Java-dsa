

public class Assignment {
    public static void main(String[] args) {
        // == equality operator
        int a = 10;
        int b = 20;
        System.out.println(a==b);
        // != inequality operator
        System.out.println(a != b);
        // > greter operator
        System.out.println(a>b);
        // < lessthan operator
        System.out.println(a<b);
        // greator then equal >=
        System.out.println(a>=b);
        // lessthan equal <=
        System.out.println(a<=b);
    }

}
