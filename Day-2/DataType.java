public class DataType {
    
    public static void main(String[] args) {
        // Primitive datatype
        // byte -> 1 byte [-128 to 127]
        byte b = 8;
        System.out.println(b);
        // char -> 2 bytes
        char ch = 'a';
        System.out.println(ch);
        // boolean -> 1 byte
        boolean var = true;
        System.out.println(var);
        // float -> 4 bytes
        float price = 10.5f;
        System.out.println(price);
        // long -> 8 bytes
        // double -> 8 bytes
        // short -> 2 bytes
        // int -> 4 bytes
        short n = 240;
        System.out.println(n);

    }
}
